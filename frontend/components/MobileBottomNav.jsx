export default function MobileBottomNav({ activeNav, setActiveNav, onMorePress }) {
  const { isMember } = useAuth();
  const navItems = isMember ? memberNavItems : adminNavItems;

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-40 bg-white dark:bg-slate-900 border-t border-slate-200 dark:border-slate-700 md:hidden safe-area-bottom">
      <div className="flex items-center justify-around px-2 py-1">
        {navItems.map((item) => {
          const isActive = item.id === "more" ? false : activeNav === item.id;
          return (
            <button
              key={item.id}
              onClick={() => item.id === "more" ? onMorePress?.() : setActiveNav(item.id)}
              className={`flex flex-col items-center gap-0.5 py-2 px-3 rounded-xl transition-all duration-200 min-w-[60px] cursor-pointer ${
                isActive
                  ? "text-success dark:text-success-400"
                  : "text-heading active:text-body"
              }`}
            >
              <div className={`${isActive ? "text-success-500" : ""}`}>
                {item.icon}
              </div>
              <span className={`text-[10px] font-medium ${isActive ? "text-success dark:text-success-400" : ""}`}>
                {item.label}
              </span>
              {isActive && (
                <div className="absolute bottom-1 w-5 h-0.5 bg-success-500 rounded-full" />
              )}
            </button>
          );
        })}
      </div>
    </nav>
  );
}
